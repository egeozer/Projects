
public class YellowToken {	//yellow token class
int x = 0;
int y =0;
	public YellowToken(){//no args cons
		
	}
	public YellowToken(int x, int y){	//constructors getting the x and y position of the token
		this.x=x;
		this.y=y;
	}
	public int getX(){	//getters for x and y position
		return x;
		
	}
	
	public int getY(){
		return y;
		
	}
}
