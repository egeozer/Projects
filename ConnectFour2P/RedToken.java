
public class RedToken {//red token class
int x = 0;
int y =0;
	public RedToken(){//no args cons
		
	}
	public RedToken(int x, int y){//constructors getting the x and y position of the token
		this.x=x;
		this.y=y;
	}
	public int getX(){ //getters for x and y position
		return x;
		
	}
	
	public int getY(){
		return y;
		
	}
}
